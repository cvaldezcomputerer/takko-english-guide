/**
 * Add a hand-grabbed image (e.g. an Irasutoya illustration) to the vocab pipeline.
 *
 * For words where stock photos don't work (emotions, tastes, abstract ideas) you
 * may download an illustration yourself. This script converts that local file to
 * the same 400x400 webp the rest of the pipeline uses and registers it in
 * tools/data/image-selections.json, so the next `download-images.mjs` run (or this
 * script itself) writes it into the generated manifest.
 *
 * Illustrations are letterboxed onto a white background (fit: "contain") so the
 * whole drawing shows instead of being cropped like a photo.
 *
 * Usage:
 *   node tools/scripts/images/add-local-image.mjs "<word>" <path-to-image> [--page <url>] [--source <name>]
 *
 * Examples:
 *   node tools/scripts/images/add-local-image.mjs "angry"  ~/Downloads/okoru.png
 *   node tools/scripts/images/add-local-image.mjs "surprised" ./o22.png --page https://www.irasutoya.com/...
 *
 * After adding what you want, the manifest is regenerated automatically.
 */

import { readFileSync, writeFileSync, existsSync, mkdirSync } from "fs";
import { join, dirname, resolve } from "path";
import { fileURLToPath } from "url";
import sharp from "sharp";
import { slugify } from "../../lib/images.mjs";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..", "..", "..");
const SELECTIONS_PATH = join(ROOT, "tools", "data", "image-selections.json");
const OUT_DIR = join(ROOT, "public", "images", "vocab");
const MANIFEST_PATH = join(ROOT, "src", "data", "vocab", "images.ts");
const CREDITS_PATH = join(OUT_DIR, "CREDITS.json");
const SIZE = 400;

// ── parse args ──
const args = process.argv.slice(2);
const flags = {};
const positional = [];
for (let i = 0; i < args.length; i++) {
  if (args[i].startsWith("--")) flags[args[i].slice(2)] = args[++i];
  else positional.push(args[i]);
}
const [word, imagePath] = positional;
if (!word || !imagePath) {
  console.error('Usage: node tools/scripts/images/add-local-image.mjs "<word>" <path-to-image> [--page <url>] [--source <name>]');
  process.exit(1);
}
const srcFile = resolve(imagePath);
if (!existsSync(srcFile)) {
  console.error(`File not found: ${srcFile}`);
  process.exit(1);
}

const source = flags.source || "irasutoya";
// Irasutoya copyright is held by Takashi Mifune; record it for CREDITS.json.
const author = flags.author || (source === "irasutoya" ? "Takashi Mifune (Irasutoya)" : null);

// ── convert the local file to a white-matted 400x400 webp ──
const slug = slugify(word);
const file = `${slug}.webp`;
const outPath = join(OUT_DIR, file);
mkdirSync(OUT_DIR, { recursive: true });

await sharp(srcFile)
  .resize(SIZE, SIZE, { fit: "contain", background: "#ffffff" })
  .flatten({ background: "#ffffff" }) // drop transparency -> solid white card
  .webp({ quality: 90 })
  .toFile(outPath);
console.log(`  wrote ${file}  (${word})`);

// ── register the selection so the manifest includes it ──
const selections = existsSync(SELECTIONS_PATH) ? JSON.parse(readFileSync(SELECTIONS_PATH, "utf8")) : {};
selections[word] = {
  id: `${source}-local`,
  source,
  manual: true, // signals: file is local, no URL to re-download from
  page: flags.page || null,
  author,
};
writeFileSync(SELECTIONS_PATH, JSON.stringify(selections, null, 2));

// ── regenerate manifest + credits from the full selections file ──
const manifest = {};
const credits = {};
for (const [en, choice] of Object.entries(selections)) {
  if (!choice || choice.skip || choice.none) continue;
  const s = slugify(en);
  if (!existsSync(join(OUT_DIR, `${s}.webp`))) continue; // only manifest images that exist on disk
  manifest[en] = `/images/vocab/${s}.webp`;
  credits[en] = { source: choice.source, author: choice.author || null, page: choice.page || null };
}
const entries = Object.keys(manifest)
  .sort()
  .map((en) => `  ${JSON.stringify(en)}: ${JSON.stringify(manifest[en])},`)
  .join("\n");
writeFileSync(
  MANIFEST_PATH,
  `// AUTO-GENERATED by tools/scripts/images/download-images.mjs — do not edit by hand.
// Maps vocab word (en) -> public image path. Regenerate after picking images.
export const vocabImages: Record<string, string> = {
${entries}
};
`
);
writeFileSync(CREDITS_PATH, JSON.stringify(credits, null, 2));
console.log(`Done. ${Object.keys(manifest).length} images in manifest.`);
