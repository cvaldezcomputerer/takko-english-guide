/**
 * Vocab audio generator — the audio sibling of the image pipeline.
 *
 * Generates one ElevenLabs voice clip per vocab word in src/data/vocab/words.ts
 * (same Alice voice as generate-audio.mjs) into public/audio/vocab/<slug>.mp3,
 * then writes the generated src/data/vocab/audio.ts manifest (en -> path).
 *
 * Requires ELEVENLABS_API_KEY in .env.
 *
 * Usage:
 *   node tools/scripts/audio/generate-vocab-audio.mjs            # all words
 *   node tools/scripts/audio/generate-vocab-audio.mjs --only apple,soda   # just these
 *
 * Safe to re-run: existing mp3s are skipped, so adding new words to words.ts and
 * re-running only generates the missing clips. The manifest is always rebuilt
 * from whatever clips currently exist on disk.
 */

import { readFileSync, writeFileSync, existsSync, mkdirSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..", "..", "..");
const OUT_DIR = join(ROOT, "public", "audio", "vocab");
const MANIFEST_PATH = join(ROOT, "src", "data", "vocab", "audio.ts");

// ── env (same manual loader as generate-audio.mjs) ──
const envPath = join(ROOT, ".env");
const envVars = Object.fromEntries(
  readFileSync(envPath, "utf8")
    .split("\n")
    .filter((l) => l.includes("=") && !l.startsWith("#"))
    .map((l) => {
      const i = l.indexOf("=");
      return [l.slice(0, i).trim(), l.slice(i + 1).trim()];
    })
);
const API_KEY = envVars.ELEVENLABS_API_KEY || process.env.ELEVENLABS_API_KEY;
if (!API_KEY) {
  console.error("Missing ELEVENLABS_API_KEY in .env");
  process.exit(1);
}

const VOICE_ID = "Xb7hH8MSUJpSbSDYk0k2"; // Alice - Clear, Engaging Educator
const MODEL_ID = "eleven_flash_v2_5";

// Slug must match tools/scripts/images/download-images.mjs so img/audio share the same key.
const slugify = (s) =>
  s
    .toLowerCase()
    .replace(/&/g, " and ")
    .replace(/['’]/g, "")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");

// ── parse the word list straight from the .ts source (no TS toolchain needed) ──
const src = readFileSync(join(ROOT, "src/data/vocab/words.ts"), "utf8");
const words = [];
const seen = new Set();
for (const line of src.split("\n")) {
  const m = line.match(/en:\s*"((?:[^"\\]|\\.)*)"\s*,\s*ja:\s*\[/);
  if (!m) continue;
  const en = m[1];
  if (seen.has(en)) continue;
  seen.add(en);
  words.push(en);
}

const onlyIdx = process.argv.indexOf("--only");
const only = onlyIdx !== -1 ? process.argv[onlyIdx + 1].split(",").map((s) => s.trim()) : null;
const targets = only ? words.filter((en) => only.includes(en)) : words;

mkdirSync(OUT_DIR, { recursive: true });

async function generate(en) {
  const slug = slugify(en);
  const outPath = join(OUT_DIR, `${slug}.mp3`);
  if (existsSync(outPath)) return "skip";

  const res = await fetch(
    `https://api.elevenlabs.io/v1/text-to-speech/${VOICE_ID}?output_format=mp3_44100_128`,
    {
      method: "POST",
      headers: { "xi-api-key": API_KEY, "Content-Type": "application/json" },
      body: JSON.stringify({
        text: `${en}.`, // trailing period → full, clear pronunciation (project convention)
        model_id: MODEL_ID,
        voice_settings: { stability: 0.85, similarity_boost: 0.75, style: 0, speed: 0.9 },
      }),
    }
  );
  if (!res.ok) {
    console.error(`  ERROR ${en}: ${res.status} ${await res.text()}`);
    return "error";
  }
  writeFileSync(outPath, Buffer.from(await res.arrayBuffer()));
  return "wrote";
}

console.log(`Generating audio for ${targets.length} word(s)...`);
let wrote = 0,
  skipped = 0;
for (const en of targets) {
  const r = await generate(en);
  if (r === "wrote") {
    wrote++;
    console.log(`  wrote ${slugify(en)}.mp3  (${en})`);
  } else if (r === "skip") skipped++;
}

// ── rebuild manifest from whatever clips exist on disk ──
const manifest = {};
for (const en of words) {
  const slug = slugify(en);
  if (existsSync(join(OUT_DIR, `${slug}.mp3`))) manifest[en] = `/audio/vocab/${slug}.mp3`;
}
const entries = Object.keys(manifest)
  .sort()
  .map((en) => `  ${JSON.stringify(en)}: ${JSON.stringify(manifest[en])},`)
  .join("\n");
writeFileSync(
  MANIFEST_PATH,
  `// AUTO-GENERATED by tools/scripts/audio/generate-vocab-audio.mjs — do not edit by hand.
// Maps vocab word (en) -> public audio path. Regenerate after adding clips.
export const vocabAudio: Record<string, string> = {
${entries}
};
`
);

console.log(`Done. ${wrote} wrote, ${skipped} skipped, ${Object.keys(manifest).length} in manifest.`);
console.log(`Manifest: ${MANIFEST_PATH}`);
